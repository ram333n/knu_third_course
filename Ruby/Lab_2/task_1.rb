def evaluate_area(points)
  return puts 'Invalid argument provided' unless points.is_a? Array
  sum = 0
  for i in 0...points.length
    next_idx = (i + 1) % points.length
    sum += (points[i][0] + points[next_idx][0]) * (points[i][1] - points[next_idx][1])
  end

  sum.abs.to_f / 2
end

